#!/bin/bash

# inicie uma sessão repl digitando node no terminal
node

# comando para ler um arquivo na sessão do nodejs no terminal
.load ./my-file.js

# para ter ajuda basta digitar .help
.help

# para salvar a sessão em um arquivo
.save ./my-session.js

# ir para modo editor multiline
.editor

# sair do modo editor ctrl + D

# sair do modo repl
.exit
